import java.util.*;

public class BitwiseOperators {
    public static void main(String args[]) {
        // Binary AND:
        System.out.println((5 & 6));
        // Binary OR:
        System.out.println((5 | 6));
        // Binary XOR:
        System.out.println((5 ^ 6));
        // Binary One's compliment Or Binary NOT:
        System.out.println((~5));
        // Binary Left Shift:
        System.out.println((5 << 2));
        // Binary Right Shift:
        System.out.println((6 >> 1));
    }
}
