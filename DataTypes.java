public class DataTypes {
    public static void main(String args[]) {
        byte b = 8; // In this byte we can only store till 256 numbers it is the range of byte.
        System.out.println(b);
        char ch = 'a'; // In this we can store all the characters but only single ones we cant write it
                       // ch='ab' : it will trow error.
        System.out.println(ch);
        boolean var = true; // It store true oe false.
        System.out.println(var);
        float price = 10.5f; // It stores all the decimal numbers. To show a float value we need to add f
                             // infront of a decimal number.
        System.out.println(price);
        int num = 56; // It store the integers.
        System.out.println(num);
        long number = 234567812; // It stores number only but used only if we want to store the bigger number.
        System.out.println(number);
        double n = 1234.789; // It stores the decimal numbers when there are bigger decimal numbers.
        System.out.println(n);
        short m = 230; // It is also used to store numbers or integers.
        System.out.println(m);
    }
}
